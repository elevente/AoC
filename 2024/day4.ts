const findXMas = (input: string[][], posX: number, posY: number, aDir: number, bDir: number): boolean => {
    if (posY - 1 < 0) {
        return false;
    }
    if (posY + 1 >= input.length) {
        return false;
    }
    if (posX - 1 < 0) {
        return false;
    }
    if (posX + 1 >= input[0].length) {
        return false;
    }
    if (input[posY][posX] != "A") {
        return false;
    }
    if (input[posY + aDir][posX + aDir] != "M") {
        return false;
    }
    if (input[posY + bDir][posX - bDir] != "M") {
        return false;
    }
    if (input[posY - aDir][posX - aDir] != "S") {
        return false;
    }
    if (input[posY - bDir][posX + bDir] != "S") {
        return false;
    }
    return true;
};

